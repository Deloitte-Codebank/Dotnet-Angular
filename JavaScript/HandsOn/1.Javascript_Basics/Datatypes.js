//Primitive Data Types
//Primitive data types include string, number, bigint, boolean, undefined, and null.
// They represent a single value and do not have properties or methods.
let name = "Santosh"; //string
let age = 30; //number
let big = 12345678901234567890n; //bigint
let isActive = true; //boolean
let x; // x is undefined
let y = null;
